    /**
     * Saves the new entered recipe
     * @param evt 
     */
    private void jbSaveRecipeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbSaveRecipeActionPerformed

        String recipeName = jtfRecipeName.getText();

        if (recipeName.equals("")) {
            JOptionPane.showMessageDialog(null, "Plese enter a recipe name");
            return;
        }

        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();

        // Map<String, Double> map = new HashMap<>();
        StringBuilder sb = new StringBuilder(String.format("%s,", recipeName));

        for (int count = 0; count < model.getRowCount(); count++) {
            String cName = model.getValueAt(count, 0).toString();
            Double cCount = Double.parseDouble(model.getValueAt(count, 1).toString());
            sb.append(String.format("%s, %s,", cName, cCount));
        }
        controller.addFood('r', sb.toString());

        modelFoods.getDataVector().removeAllElements();

        jcbConsumed.removeAllItems();
        displayFoodItems(controller.getAllFoods());
        jdAddRecipe.setVisible(false); // hides addRecipe dialog

        // resets add component fields
        jtfComponentCount.setText("");
        jcbComponent.setSelectedIndex(0);

        // resets recipe name and teble
        jtfRecipeName.setText("");
        model.getDataVector().removeAllElements();
    }//GEN-LAST:event_jbSaveRecipeActionPerformed
    
