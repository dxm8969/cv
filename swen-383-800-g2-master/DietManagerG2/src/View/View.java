    /**
     * Saves the entered food into the food list
     * @param evt 
     */
    private void jbSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbSaveActionPerformed
        // TODO add your handling code here:
        String foodName = jtfFoodName.getText();
        String carbs = jtfCarb.getText();
        String fat = jtfFat.getText();
        String protein = jtfProtein.getText();
        String calories = jtfCalories.getText();

        // violates dry
        jtfFat.getText().isEmpty();
        if (jtfFat.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "You need to enter a number for fat");
            return;
        }

        if (jtfCarb.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "You need to enter a number for carb");
            return;
        }

        if (jtfProtein.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "You need to enter a number for protein");
            return;
        }

        if (jtfCalories.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "You need to enter a number for calories");
            return;
        }

        try {
            Double.parseDouble(carbs);
            Double.parseDouble(fat);
            Double.parseDouble(protein);
            Double.parseDouble(calories);

            String data = String.format("%s, %s, %s, %s, %s", foodName, calories, fat, carbs, protein);

            // Cends data to ResourceHandler
            controller.addFood('b', data);

            modelFoods.getDataVector().removeAllElements();

            jcbConsumed.removeAllItems();
            jcbComponent.removeAllItems();
            displayFoodItems(controller.getAllFoods());

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "You need to enter a nuber for calories, fat, protein and carbs");

        }

        jtfFoodName.setText("");
        jtfCarb.setText("");
        jtfFat.setText("");
        jtfProtein.setText("");
        jtfCalories.setText("");
        jdAddBasicFood.setVisible(false);

    }//GEN-LAST:event_jbSaveActionPerformed
    
