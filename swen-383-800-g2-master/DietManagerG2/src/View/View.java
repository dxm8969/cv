    /**
     * Adds a Model.ModelFood.BasicFood or Model.ModelFood.Recipe to UTIL.Foods
     */
    public void addFood(char type, String data) {
        controller.addFood(type, data);
    }

    /**
     * Displays all of the food items into the food table
     */
    public void displayFoodItems(ArrayList<String> foodNames) {
        for (String name : foodNames) {
            String[] data = {name};
            modelFoods.addRow(data);
            jcbConsumed.addItem(name);
            jcbComponent.addItem(name);
        }
    }

    /**
     * Gets info about a Model.ModelFood.BasicFood or Model.ModelFood.Recipe
     */
    public ArrayList getFoodInfo(String name) {

        return controller.getFoodInfo(name);
    }


    /**
     * Adds a Model.ModelFood.BasicFood or Model.ModelFood.Recipe to the
     *
     * @param day desired day
     * @param name food name
     */
    public void addToIntake(String day, String name) {
        controller.addToIntake(day, name);
    }

    /**
     * Gets the info for a specific intake based on the entered date
     */
    public void displayIntakeInfo() {
        ArrayList<Object> info = controller.getIntakeInfo(getDateInput());

        jtfCalorieLimit.setText((String) info.get(0));
        jtfWeight.setText((String) info.get(1));

        jtfCurrCalories.setText((String) info.get(2)); // updates calories
        jtfCurrFat.setText((String) info.get(3)); // updates fat
        jtfCurrProtein.setText((String) info.get(4)); // updates protein
        jtfCurrCarb.setText((String) info.get(5)); // updates carb

        int rowNum = modelConsumedFoods.getRowCount();
        for (int i = rowNum - 1; i >= 0; i--) {
            modelConsumedFoods.removeRow(i);
        }

        for (String name : (ArrayList<String>) info.get(6)) {
            String[] data = {name};
            modelConsumedFoods.addRow(data);
        }

    }
    
    /**
     * Gets the entered date
     * @return the date in a string format
     */
    public String getDateInput() {
        String day = jtfDay.getText();
        String month = jtfMonth.getText();
        String year = jtfYear.getText();

        return year + "/" + month + "/" + day;
    }

